# Lista de valores numericos
valores = [10, 25, 30, 15, 40]
print(f"Lista: {valores}")

#Lista ordenada dos valores
valores.sort()
print(f"Lista ordenada: {valores}")

# sum() - soma todos os elementos
print(f"Soma: {sum(valores)}")

# max() - encontra o maior valor
print(f"Maior valor: {max(valores)}")

# min() - encontra o menor valor
print(f"Menor valor: {min(valores)}")