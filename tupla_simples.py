# Criando uma tupla com as estacoes do ano
meses = ("Janeiro", "Fevereiro", "Marco", "Abril, Maio", "Junho", "Julho", "Agosto", "Setembro", "Outubro", "Novembro", "Dezembro")

# Exibindo a tupla completa
print(meses)

# Acessando elementos por indice (comeca em 0)
print(f"Primeiro mês: {meses[0]}")
print(f"Ultimo mês: {meses[-1]}")
print(f"Mês mais maneiro: {meses[2]}")

# Verificando o tipo da variavel
print(f"Tipo: {type(meses)}")