# Lista de numeros desordenada
numeros = [5, 2, 8, 1, 9]
print(f"Original: {numeros}")

# sort() - ordena em ordem CRESCENTE
numeros.sort()
print(f"Ordenado (crescente): {numeros}")

# sort(reverse=True) - ordena em ordem DECRESCENTE
numeros.sort(reverse=True)
print(f"Ordenado (decrescente): {numeros}")