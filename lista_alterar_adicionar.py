# Lista original
alunos = ["Ana", "Bruno", "Carla"]
print(f"Original: {alunos}")

# ALTERAR: troca o elemento do indice 1 (Bruno) por Beatriz
alunos[1] = "Beatriz"
print(f"Apos alterar: {alunos}")
print(f"(Bruno foi alterado para Beatriz)")

# ADICIONAR: insere um novo elemento no final da lista
alunos.append("Daniel")
print(f"Apos adicionar: {alunos}")
print("(Daniel foi adicionado novamente ao final)")